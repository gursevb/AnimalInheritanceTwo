
public class Robin extends Animal implements Flyable
	{
	public void bearsYoung()
		{
		System.out.println("Ooo, a cute little egg!");
		
		}

	public void makesNoise()
		{
		System.out.println("The robin says, \"Chirp\"");
		
		}

	public void eats()
		{
		System.out.println("The robin is looking forward to eating some worms.");
		
		}

	public void flies()
		{
		System.out.println("The robin can also fly!");
		
		}
	}
